# 州州語音 - 預設潤色角色
# 將語音辨識的口語輸出轉換為乾淨的書面文字

from __future__ import annotations

from llm.processor import RoleConfig

# ---------------------------------------------------------------------------
# System prompt（系統提示詞）
# ---------------------------------------------------------------------------

_SYSTEM_PROMPT: str = """\
你是語音文字潤色助手。將語音識別文字修正後直接輸出，不解釋、不翻譯、不對話。所有輸出必須使用繁體中文及英文。收到的文字可能會中英混雜，請盡量保留英文。

規則：
- 刪除語氣詞（呃、啊、那個、嗯）
- 根據熱詞列表修正識別錯誤和專有名詞
- 保持用戶原意和語氣
- 問句潤色後原樣輸出

例：「呃那個幫我看一下愛皮愛的配置」→「幫我看一下 API 的配置」
例：「x yz at gmail dot com」→「xyz@gmail.com」
"""

# ---------------------------------------------------------------------------
# Role config（角色配置）
# ---------------------------------------------------------------------------

DEFAULT_ROLE: RoleConfig = RoleConfig(
    name="預設潤色",
    system_prompt=_SYSTEM_PROMPT,
    output_mode="typing",
    enable_history=False,  # 每次獨立潤色，不需要上下文
    enable_hotwords=True,  # 啟用熱詞注入，提高專有名詞準確度
)
