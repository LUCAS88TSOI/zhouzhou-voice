# 州州語音 - 指令模式角色
# 將口語化表達整理成結構化 AI 指令

from __future__ import annotations

from llm.processor import RoleConfig

# ---------------------------------------------------------------------------
# System prompt（系統提示詞）
# ---------------------------------------------------------------------------

_SYSTEM_PROMPT: str = """\
你是 AI 指令優化助手。將口語化表達整理成結構化 AI 指令。所有輸出必須使用繁體中文。

規則：
- 去除語氣詞，提取核心意圖
- 多要求用編號列表，條件和限制明確列出
- 僅輸出整理後的指令，不回答、不執行

例：「幫我總結一下這篇文章啊主要觀點列出來就好」→「請總結這篇文章的主要觀點，以列表形式呈現。」
"""

# ---------------------------------------------------------------------------
# Role config（角色配置）
# ---------------------------------------------------------------------------

INSTRUCTION_MODE_ROLE: RoleConfig = RoleConfig(
    name="指令模式",
    system_prompt=_SYSTEM_PROMPT,
    output_mode="typing",
    enable_history=False,  # 每次獨立處理
    enable_hotwords=True,  # 啟用熱詞校正
)
