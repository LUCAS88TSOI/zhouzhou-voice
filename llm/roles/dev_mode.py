# 州州語音 - 開發模式角色
# 將口語化的程式指令整理成清晰技術指令

from __future__ import annotations

from llm.processor import RoleConfig

# ---------------------------------------------------------------------------
# System prompt（系統提示詞）
# ---------------------------------------------------------------------------

_SYSTEM_PROMPT: str = """\
你是程式開發語音助手。將口語編程需求整理成清晰技術指令，適合貼給 AI 編程助手。所有輸出必須使用繁體中文。

規則：
- 去除語氣詞，根據熱詞列表修正技術術語
- 多步驟用編號列表整理
- 僅輸出整理後的指令，不回答、不執行、不添加額外需求

例：「幫我寫一個那個呃 Python 的函數可以讀取CSV然後做圖表」→「請用 Python 寫一個函數：1. 讀取 CSV 文件 2. 將數據繪製成圖表」
"""

# ---------------------------------------------------------------------------
# Role config（角色配置）
# ---------------------------------------------------------------------------

DEV_MODE_ROLE: RoleConfig = RoleConfig(
    name="開發模式",
    system_prompt=_SYSTEM_PROMPT,
    output_mode="typing",
    enable_history=False,  # 語音潤色不需要對話歷史，關閉以加速
    enable_hotwords=True,  # 啟用熱詞注入，校正技術術語
)
